# To convert "레이스.py" into an executable file, you can use the `pyinstaller` library.
# Install pyinstaller using pip:
# pip install pyinstaller

# Then, run the following command in your terminal or command prompt:
# pyinstaller --onefile --noconsole 레이스.py

# Explanation:
# --onefile: Packages everything into a single executable file.
# --noconsole: Hides the console window when running the executable (useful for GUI applications).

# After running the command, the executable will be located in the "dist" folder.